#include <stdio.h>


// constant
const float pie = 3.14159;

void main() {

    int age = 19;           
    char grade = 'A';       
    float height = 5.5;      
    
    printf("=== Variables and Constants in C ===\n");
    printf("Age (int) : %d\n", age);
    printf("Grade (char) : %c\n", grade);
    printf("Height (float) : %0.2f\n", height);
    printf("Constant PI : %0.5f\n", pie);

}